    private class ArcRenderer implements bka.awt.Renderer {

        public ArcRenderer(double radius, Paint paint, SolarDecorator.Arc arc) {
            this.radius = radius;
            this.paint = paint;
            this.arc = arc;
        }

        @Override
        public void paint(Graphics2D graphics) {
            if (arc.getStart().isPresent() && arc.getEnd().isPresent()) {
                graphics.setPaint(paint);
                graphics.setStroke(ARC_STROKE);
                graphics.draw(clock.createArc(radius, arc.getStart().get(), arc.getEnd().get()));
            }
        }


        private final double radius;
        private final Paint paint;
        private final SolarDecorator.Arc arc;
    }


    private class ClockPanel extends javax.swing.JPanel {
        @Override
        public void paint(Graphics graphics) {
            super.paint(graphics);
            clock.paint((Graphics2D) graphics);
        }
    }


