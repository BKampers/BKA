    public void update() {
        yearLabel.setText(model.getYear());
        weekLabel.setText(model.getWeek());
        setFontColor(dayLabel, model.getDayOfWeekColor());
        dayLabel.setText(model.getDayOfWeek());
        dateLabel.setText(model.getDate());
        monthLabel.setText(model.getMonth());
        setFontColor(dayOfYearLabel, model.getDayOfYearColor());
        dayOfYearLabel.setText(model.getDayOfYear().orElse(""));
        yearProgressBar.setMaximum(model.getYearDayMax());
        yearProgressBar.setValue(model.getYearDay());
        hourHand.setValue(model.getHour());
        minuteHand.setValue(model.getMinute());
        secondHand.setValue(model.getSecond());
        decorator.ifPresent(this::updateSolarDecoration);
        repaint();
        datePanel.setToolTipText(model.getDateToolTipText());
        clockPanel.setToolTipText(model.getTimeToolTipText());
        yearProgressBar.setToolTipText(model.getDayOfYearToolTipText());
    }


    private void updateSolarDecoration(SolarDecorator solarDecorator) {
        SolarDecorator.Period currentPeriod = currentPeriod(solarDecorator);
        Paint arcPaint = arcPaints.get(currentPeriod);
        if (!clockFacePaint.equals(arcPaint)) {
            updateColors(currentPeriod, arcPaint);
        }
        solarDecorator.calculateArcs(LocalDate.now());
    }

    private SolarDecorator.Period currentPeriod(SolarDecorator solarDecorator) {
        double currentHour = model.getHour();
        SortedMap<SolarDecorator.Period, SolarDecorator.Arc> arcs = solarDecorator.getArcs();
        return arcs.entrySet().stream()
            .filter(lessThan(currentHour))
            .map(Map.Entry::getKey)
            .findFirst()
            .orElse(arcs.firstKey());
    }

    private static Predicate<Map.Entry<SolarDecorator.Period, SolarDecorator.Arc>> lessThan(double currentHour) {
        return entry -> {
            Optional<Double> end = entry.getValue().getEnd();
            return end.isPresent() && currentHour < end.get();
        };
    }

    private void updateColors(SolarDecorator.Period currentEvent, Paint paint) {
        clockFacePaint = paint;
        hourHandPaint = (currentEvent == SolarDecorator.Period.NIGHTTIME) ? BRIGHT_COLOR : Color.BLACK;
        minuteHandPaint = (currentEvent == SolarDecorator.Period.NIGHTTIME) ? BRIGHT_COLOR : Color.BLACK;
        secondHandPaint = (currentEvent == SolarDecorator.Period.NIGHTTIME) ? Color.YELLOW : Color.RED;
        numberRenderer.setPaint((currentEvent == SolarDecorator.Period.NIGHTTIME) ? Color.YELLOW : Color.BLUE);
        markerPaint = (currentEvent == SolarDecorator.Period.NIGHTTIME) ? BRIGHT_COLOR : Color.BLUE;
    }

    private void setFontColor(javax.swing.JLabel component, Optional<Color> color) {
        component.setForeground(color.orElse(FONT_COLOR));
    }

